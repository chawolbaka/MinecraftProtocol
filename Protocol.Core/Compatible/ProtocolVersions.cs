        public static void WriteRaw(IDictionary<string, int> source)
        {
            Type t = typeof(ProtocolVersions);
            foreach (var c in t.GetFields())
            {
                source.Add(
                    key: c.Name
                    .TrimStart('V')
                    .Replace("_pre", " Pre-Release ")
                    .Replace("M_M", "-")
                    .Replace("__", " ")
                    .Replace('_', '.')
                    .Replace("INFINITAS", "∞"),
                    value: (int)c.GetValue(null)); ;
            }
        }
