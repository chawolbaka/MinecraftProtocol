        private static Dictionary<string, int> SearchTable;
        public static int SearchByName(string name)
        {
            SearchTable ??= GetNameIdDictionary();
            if (SearchTable.ContainsKey(name))
                return SearchTable[name];
            else
                return -1;
        }
        
        public static IEnumerable<string> GetNames() => (SearchTable ??= GetNameIdDictionary()).Keys;

        public static IEnumerable<int> GetIds() => (SearchTable ??= GetNameIdDictionary()).Values;

        public static IEnumerable<(string Name, ProtocolVersionType Type, int Id)> GetEnumerator()
        {
            Type t = typeof(ProtocolVersions);
            foreach (var c in t.GetFields())
            {
                object[] attributes = c.GetCustomAttributes(false);
                if (attributes is not null && attributes.Length > 0 && attributes[0] is ProtocolVersionAttribute attribute)
                    yield return (attribute.Name, attribute.Type, (int)c.GetValue(null));          
            }
        }

        private static Dictionary<string,int> GetNameIdDictionary()
        {
            Dictionary<string, int> ProtocolVersionDictionary = new Dictionary<string, int>();
            foreach (var item in GetEnumerator())
            {
                ProtocolVersionDictionary.Add(item.Name,item.Id);
            }
            return ProtocolVersionDictionary;
        }

